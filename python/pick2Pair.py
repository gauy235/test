def 乙輪抓牌(乙輪牌箱, 甲乙輪的收集, 乙輪第幾抽):
		
	if 乙輪第幾抽 == len(乙輪牌箱):
		 if len(甲乙輪的收集) == 一對張數+一對張數:
		 	if 甲乙輪的收集[2][-1:]==甲乙輪的收集[3][-1:] and 甲乙輪的收集[2][-1:]!=甲乙輪的收集[0][-1:]:
		 		
		 		剩下的牌=排除元素(甲輪牌箱, 甲乙輪的收集)
		 		for 單張牌 in 剩下的牌:
		 			if 單張牌[-1:]!=甲乙輪的收集[0][-1:] and 單張牌[-1:]!=甲乙輪的收集[3][-1:]:
		 				print("抓2Pair="+ str(甲乙輪的收集)+" 搭 "+單張牌)
		 return # 上帝指示

	乙輪抓牌(乙輪牌箱, 甲乙輪的收集, 乙輪第幾抽+1) # 問抓不抓? 不抓		
	拷貝那份=甲乙輪的收集.copy()
	拷貝那份.append(乙輪牌箱[乙輪第幾抽]) # 加進去
	乙輪抓牌(乙輪牌箱, 拷貝那份, 乙輪第幾抽+1) # 問抓不抓? 抓

甲輪牌箱=['紅心2', '梅花3', '方塊3', '黑桃9', '紅心9', '方塊9', '梅花9']
一對張數= 2

def 甲輪抓牌(甲輪的收集, 甲輪第幾抽):

	if 甲輪第幾抽 == len(甲輪牌箱):
		 if len(甲輪的收集) == 一對張數:
		 	if 甲輪的收集[0][-1:]==甲輪的收集[1][-1:]:
		 		剩下的牌=排除元素(甲輪牌箱, 甲輪的收集)
		 		乙輪抓牌(剩下的牌, 甲輪的收集, 0) # 再抓 1 輪
		 return # 上帝指示

	甲輪抓牌(甲輪的收集, 甲輪第幾抽+1) # 問抓不抓? 不抓
	拷貝那份=甲輪的收集.copy()
	拷貝那份.append(甲輪牌箱[甲輪第幾抽]) # 加進去
	甲輪抓牌(拷貝那份, 甲輪第幾抽+1) # 問抓不抓? 抓
